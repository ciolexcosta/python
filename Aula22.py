# Operadores de comparação, retornam true or false
'''
    == Igual
    != Diferente
    >  Maior que
    <  Menor que
    >= Maior igual a
    <= Menor igual a
'''

operadores = 10 == 11  # serve para numeros e textos, case sensitive
operadores2 = 'banana' != 'Banana'
operadores3 = 10 > 50
operadores4 = 10 < 11
operadores5 = 10 >= 11
operadores6 = 10 <= 10

print(operadores)
print(operadores2)
print(operadores3)
print(operadores4)
print(operadores5)
print(operadores6)

